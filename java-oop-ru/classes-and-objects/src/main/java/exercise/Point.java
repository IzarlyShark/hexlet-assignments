package exercise;

// BEGIN
public class Point {
    private final int x; // Координата X
    private final int y; // Координата Y

    // Конструктор
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Метод для получения координаты X
    public int getX() {
        return x;
    }

    // Метод для получения координаты Y
    public int getY() {
        return y;
    }
}
// END
